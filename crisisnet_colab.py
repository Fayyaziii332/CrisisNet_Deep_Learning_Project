# ── InfoNCE (CLIP-style) loss ────────────────────────────────────
class InfoNCELoss(nn.Module):
    def forward(self, img, txt, scale):
        """img, txt: L2-normalised (B, D). scale: scalar temperature."""
        logits = scale * img @ txt.T   # (B, B)
        labels = torch.arange(len(img), device=img.device)
        loss_i = F.cross_entropy(logits,   labels)
        loss_t = F.cross_entropy(logits.T, labels)
        return (loss_i + loss_t) / 2


# ── Multi-task loss ───────────────────────────────────────────────
class MultiTaskLoss(nn.Module):
    def __init__(self, w_info=1.0, w_dmg=0.5, w_hum=0.5):
        super().__init__()
        self.w = {'informative': w_info, 'damage': w_dmg, 'humanitarian': w_hum}

    def forward(self, logits: dict, batch: dict):
        total = 0.0
        key_map = {
            'informative': 'inform_label',
            'damage':      'damage_label',
            'humanitarian':'human_label',
        }
        for task, lbl_key in key_map.items():
            if lbl_key in batch:
                labels = batch[lbl_key].to(device)
                total += self.w[task] * F.cross_entropy(logits[task], labels)
        return total


# ── Evaluation ────────────────────────────────────────────────────
@torch.no_grad()
def evaluate(model, loader, stage='finetune'):
    model.eval()
    preds = {'informative': [], 'damage': [], 'humanitarian': []}
    trues = {'informative': [], 'damage': [], 'humanitarian': []}
    key_map = {
        'informative': 'inform_label',
        'damage':      'damage_label',
        'humanitarian':'human_label',
    }

    for batch in tqdm(loader, desc='Evaluating', leave=False):
        images = batch['image'].to(device)
        ids    = batch['input_ids'].to(device)
        mask   = batch['attention_mask'].to(device)

        if stage == 'dann':
            logits, _ = model(images, ids, mask, alpha=0.0)
        else:
            logits    = model(images, ids, mask)

        for task, lbl_key in key_map.items():
            if lbl_key in batch:
                pred = logits[task].argmax(dim=1).cpu().numpy()
                true = batch[lbl_key].numpy()
                preds[task].extend(pred)
                trues[task].extend(true)

    metrics = {}
    for task in preds:
        if len(preds[task]) == 0: continue
        p, t = np.array(preds[task]), np.array(trues[task])
        metrics[f'{task}_f1']  = f1_score(t, p, average='macro', zero_division=0)
        metrics[f'{task}_acc'] = accuracy_score(t, p)

    if metrics:
        avg_f1 = np.mean([v for k, v in metrics.items() if '_f1' in k])
        metrics['avg_f1'] = avg_f1

    model.train()
    return metrics


def print_metrics(metrics: dict, epoch: int):
    hdr = f"  Epoch {epoch:03d}"
    for k, v in metrics.items():
        hdr += f"  |  {k}: {v:.4f}"
    print(hdr)


