"""## 🧠 Cell 8 — Model Architecture
**ImageEncoder + TextEncoder + CrossModalTransformer + CrisisNet + DANN**
"""

# ══════════════════════════════════════════════════════════════
# 1. IMAGE ENCODER  (ResNet-50 backbone + linear projection)
# ══════════════════════════════════════════════════════════════
class ImageEncoder(nn.Module):
    def __init__(self, proj_dim=512):
        super().__init__()
        base = models.resnet50(weights=models.ResNet50_Weights.IMAGENET1K_V2)
        # Keep spatial feature map (B, 2048, 7, 7) — drop avgpool + fc
        self.backbone   = nn.Sequential(*list(base.children())[:-2])
        self.global_pool= nn.AdaptiveAvgPool2d(1)
        self.proj       = nn.Sequential(nn.Linear(2048, proj_dim), nn.LayerNorm(proj_dim))

    def forward(self, x, mode='spatial'):
        feats = self.backbone(x)                     # (B, 2048, 7, 7)
        if mode == 'global':
            g = self.global_pool(feats).flatten(1)   # (B, 2048)
            return self.proj(g)                      # (B, proj_dim)
        # spatial tokens for cross-attention
        B, C, H, W = feats.shape
        tokens = feats.permute(0,2,3,1).reshape(B, H*W, C)  # (B, 49, 2048)
        return self.proj(tokens)                     # (B, 49, proj_dim)


# ══════════════════════════════════════════════════════════════
# 2. TEXT ENCODER  (BERT-base + linear projection)
# ══════════════════════════════════════════════════════════════
class TextEncoder(nn.Module):
    def __init__(self, proj_dim=512, bert_name='bert-base-uncased'):
        super().__init__()
        self.bert = BertModel.from_pretrained(bert_name)
        self.proj = nn.Sequential(nn.Linear(768, proj_dim), nn.LayerNorm(proj_dim))

    def forward(self, input_ids, attention_mask, mode='sequence'):
        out = self.bert(input_ids=input_ids, attention_mask=attention_mask)
        hidden = out.last_hidden_state                # (B, L, 768)
        projected = self.proj(hidden)                 # (B, L, proj_dim)
        if mode == 'cls':
            return projected[:, 0, :]                # (B, proj_dim)
        return projected                              # (B, L, proj_dim)


# ══════════════════════════════════════════════════════════════
# 3. CROSS-MODAL TRANSFORMER  (bidirectional cross-attention)
# ══════════════════════════════════════════════════════════════
class CrossModalLayer(nn.Module):
    """One cross-attention + FFN block (query attends to kv)."""
    def __init__(self, dim, heads, dropout=0.1):
        super().__init__()
        self.attn  = nn.MultiheadAttention(dim, heads, dropout=dropout, batch_first=True)
        self.norm1 = nn.LayerNorm(dim)
        self.ffn   = nn.Sequential(
            nn.Linear(dim, dim*4), nn.GELU(), nn.Dropout(dropout),
            nn.Linear(dim*4, dim), nn.Dropout(dropout),
        )
        self.norm2 = nn.LayerNorm(dim)
        self.drop  = nn.Dropout(dropout)

    def forward(self, q, kv, kv_mask=None):
        a, w = self.attn(q, kv, kv, key_padding_mask=kv_mask,
                         need_weights=True, average_attn_weights=True)
        q = self.norm1(q + self.drop(a))
        q = self.norm2(q + self.ffn(q))
        return q, w


class CrossModalTransformer(nn.Module):
    def __init__(self, dim, heads, n_layers=2, dropout=0.1):
        super().__init__()
        # Image → Text cross-attention
        self.i2t = nn.ModuleList([CrossModalLayer(dim, heads, dropout) for _ in range(n_layers)])
        # Text → Image cross-attention
        self.t2i = nn.ModuleList([CrossModalLayer(dim, heads, dropout) for _ in range(n_layers)])
        # Self-attention (image)
        self.isa = nn.ModuleList([
            nn.TransformerEncoderLayer(dim, heads, dim*4, dropout, batch_first=True, norm_first=True)
            for _ in range(n_layers)
        ])
        # Self-attention (text)
        self.tsa = nn.ModuleList([
            nn.TransformerEncoderLayer(dim, heads, dim*4, dropout, batch_first=True, norm_first=True)
            for _ in range(n_layers)
        ])
        self.norm = nn.LayerNorm(dim)

    def forward(self, img_tok, txt_tok, txt_pad=None):
        """
        img_tok : (B, 49, D)   text_tok : (B, L, D)
        txt_pad : (B, L) True=padding
        Returns : fused (B, D), last cross-attn weights (B, 49, L)
        """
        attn_w = None
        for i2t_l, t2i_l, isa_l, tsa_l in zip(self.i2t, self.t2i, self.isa, self.tsa):
            img_tok, attn_w = i2t_l(img_tok, txt_tok, txt_pad)
            txt_tok, _      = t2i_l(txt_tok, img_tok)
            img_tok = isa_l(img_tok)
            txt_tok = tsa_l(txt_tok, src_key_padding_mask=txt_pad)

        combined = torch.cat([img_tok, txt_tok], dim=1)  # (B, 49+L, D)
        fused    = self.norm(combined.mean(dim=1))        # (B, D)
        return fused, attn_w


# ══════════════════════════════════════════════════════════════
# 4. TASK HEADS (three independent classifiers)
# ══════════════════════════════════════════════════════════════
class TaskHeads(nn.Module):
    def __init__(self, dim, n_info, n_dmg, n_hum, dropout=0.1):
        super().__init__()
        def _head(n):
            return nn.Sequential(
                nn.Dropout(dropout), nn.Linear(dim, dim//2),
                nn.GELU(), nn.Dropout(dropout), nn.Linear(dim//2, n)
            )
        self.info = _head(n_info)
        self.dmg  = _head(n_dmg)
        self.hum  = _head(n_hum)

    def forward(self, x):
        return {'informative': self.info(x),
                'damage':      self.dmg(x),
                'humanitarian':self.hum(x)}


# ══════════════════════════════════════════════════════════════
# 5. FULL CRISISNET MODEL
# ══════════════════════════════════════════════════════════════
class CrisisNet(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.img_enc = ImageEncoder(cfg.proj_dim)
        self.txt_enc = TextEncoder(cfg.proj_dim, cfg.bert_name)
        self.fusion  = CrossModalTransformer(cfg.proj_dim, cfg.num_heads,
                                              cfg.num_layers, cfg.dropout)
        self.heads   = TaskHeads(cfg.proj_dim, cfg.n_inform, cfg.n_damage,
                                  cfg.n_human, cfg.dropout)

    def forward(self, images, input_ids, attention_mask, return_attn=False):
        img_tok  = self.img_enc(images, mode='spatial')
        txt_tok  = self.txt_enc(input_ids, attention_mask, mode='sequence')
        txt_pad  = (attention_mask == 0)
        fused, w = self.fusion(img_tok, txt_tok, txt_pad)
        out      = self.heads(fused)
        return (out, w) if return_attn else out


# ══════════════════════════════════════════════════════════════
# 6. CLIP-LIKE MODEL  (for Stage 1 SSL — global features only)
# ══════════════════════════════════════════════════════════════
class CLIPModel(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.img_enc    = ImageEncoder(cfg.proj_dim)
        self.txt_enc    = TextEncoder(cfg.proj_dim, cfg.bert_name)
        self.log_scale  = nn.Parameter(torch.ones([]) * math.log(1 / cfg.ssl_temp))

    def forward(self, images, input_ids, attention_mask):
        img = F.normalize(self.img_enc(images, mode='global'), dim=-1)
        txt = F.normalize(self.txt_enc(input_ids, attention_mask, mode='cls'), dim=-1)
        return img, txt, self.log_scale.exp().clamp(max=100)


# ══════════════════════════════════════════════════════════════
# 7. DANN MODEL  (Stage 3 — domain-adversarial neural network)
# ══════════════════════════════════════════════════════════════
class GradRevFn(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x, alpha):
        ctx.alpha = alpha; return x
    @staticmethod
    def backward(ctx, g):
        return -ctx.alpha * g, None

class GradRev(nn.Module):
    def forward(self, x, alpha=1.0):
        return GradRevFn.apply(x, alpha)

class CrisisNetDANN(CrisisNet):
    def __init__(self, cfg):
        super().__init__(cfg)
        self.grl     = GradRev()
        self.dom_cls = nn.Sequential(
            nn.Linear(cfg.proj_dim, 256), nn.ReLU(),
            nn.Dropout(cfg.dropout),     nn.Linear(256, cfg.n_disasters)
        )

    def forward(self, images, input_ids, attention_mask, alpha=1.0, return_attn=False):
        img_tok  = self.img_enc(images, mode='spatial')
        txt_tok  = self.txt_enc(input_ids, attention_mask, mode='sequence')
        txt_pad  = (attention_mask == 0)
        fused, w = self.fusion(img_tok, txt_tok, txt_pad)
        tasks    = self.heads(fused)
        domain   = self.dom_cls(self.grl(fused, alpha))
        if return_attn:
            return tasks, domain, w
        return tasks, domain


