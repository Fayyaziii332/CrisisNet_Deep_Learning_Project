class CheckpointManager:
    """
    Saves checkpoints to Drive after every epoch.
    On session restart: call .resume() to continue from where you left off.
    """
    def __init__(self, stage: str, model_name: str):
        # Primary: Drive. Fallback: local /content
        self.primary = cfg.ckpt(stage) if cfg.use_drive else f'/content/checkpoints/{stage}'
        self.local   = f'/content/checkpoints/{stage}'
        self.name    = model_name
        os.makedirs(self.primary, exist_ok=True)
        os.makedirs(self.local,   exist_ok=True)
        self.history: List[dict] = []
        self._best_val = None

    # ── Save ────────────────────────────────────────────────────
    def save(self, model, optimizer, epoch, metrics: dict,
             scheduler=None, higher_is_better=True):
        ckpt = {
            'epoch': epoch,
            'model': model.state_dict(),
            'optim': optimizer.state_dict(),
            'metrics': metrics,
            'ts': time.time(),
        }
        if scheduler:
            ckpt['sched'] = scheduler.state_dict()

        # Determine if best
        key   = next(iter(metrics))          # first metric is the primary one
        val   = metrics[key]
        is_best = (self._best_val is None or
                   (higher_is_better and val > self._best_val) or
                   (not higher_is_better and val < self._best_val))
        if is_best:
            self._best_val = val

        # Write files
        for base in [self.primary, self.local]:
            self._write(ckpt, os.path.join(base, f'{self.name}_latest.pth'))
            if is_best:
                self._write(ckpt, os.path.join(base, f'{self.name}_best.pth'))

        # History
        self.history.append({'epoch': epoch, **metrics})
        hist_path = os.path.join(self.primary, 'history.json')
        with open(hist_path, 'w') as f:
            json.dump(self.history, f, indent=2)

        tag = " ⭐ BEST" if is_best else ""
        print(f"  💾 Saved epoch {epoch:03d} → {self.primary}{tag}")
        return is_best

    def _write(self, obj, path):
        tmp = path + '.tmp'
        torch.save(obj, tmp)
        os.replace(tmp, path)   # atomic rename — safe against partial writes

    # ── Load / Resume ────────────────────────────────────────────
    def resume(self, model, optimizer=None, scheduler=None, best=False):
        """Load checkpoint. Returns (start_epoch, metrics)."""
        tag  = 'best' if best else 'latest'
        name = f'{self.name}_{tag}.pth'
        # Try Drive first, then local
        for base in [self.primary, self.local]:
            path = os.path.join(base, name)
            if os.path.exists(path):
                return self._load(path, model, optimizer, scheduler)
        print(f"  No checkpoint found ({name}). Starting from scratch.")
        return 0, {}

    def _load(self, path, model, optimizer, scheduler):
        print(f"  📂 Loading: {path}")
        # Added weights_only=False to address UnpicklingError in PyTorch >= 2.6
        ckpt = torch.load(path, map_location=device, weights_only=False)

        # Strip DataParallel prefix if present
        sd = {k.replace('module.', ''): v for k, v in ckpt['model'].items()}
        missing, unexpected = model.load_state_dict(sd, strict=False)
        if missing:    print(f"     Missing keys  : {missing[:3]}")
        if unexpected: print(f"     Unexpected keys: {unexpected[:3]}")

        if optimizer and 'optim' in ckpt:
            try:    optimizer.load_state_dict(ckpt['optim'])
            except: print("     ⚠️  Optimizer state mismatch — using fresh.")
        if scheduler and 'sched' in ckpt:
            try:    scheduler.load_state_dict(ckpt['sched'])
            except: pass

        epoch   = ckpt.get('epoch', 0)
        metrics = ckpt.get('metrics', {})
        print(f"     Resumed from epoch {epoch}, metrics={metrics}")
        return epoch, metrics

