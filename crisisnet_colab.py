def train_ssl(resume: bool = True):
    """Stage 1: CLIP-style contrastive pre-training."""
    print("\n" + "="*60)
    print("STAGE 1 — SSL Pre-training")
    print("="*60)

    model = CLIPModel(cfg).to(device)
    loss_fn = InfoNCELoss()
    ckpt_mgr = CheckpointManager('ssl', 'clip')

    ssl_loader = make_loader(ssl_ds, cfg.ssl_bs, shuffle=True)
    total_steps = len(ssl_loader) * cfg.ssl_epochs

    optimizer = AdamW(model.parameters(), lr=cfg.ssl_lr, weight_decay=cfg.ssl_wd)
    scheduler = get_linear_schedule_with_warmup(
        optimizer,
        num_warmup_steps  = max(1, int(0.05 * total_steps)),
        num_training_steps= total_steps,
    )
    scaler = GradScaler(enabled=cfg.use_amp)

    # ── Resume ────────────────────────────────────────────────────
    start_epoch = 0
    if resume:
        start_epoch, _ = ckpt_mgr.resume(model, optimizer, scheduler)

    best_loss = float('inf')

    for epoch in range(start_epoch, cfg.ssl_epochs):
        model.train()
        run_loss = 0.0
        pbar = tqdm(ssl_loader, desc=f'SSL {epoch+1:02d}/{cfg.ssl_epochs}', leave=False)

        for batch in pbar:
            images = batch['image'].to(device, non_blocking=True)
            ids    = batch['input_ids'].to(device, non_blocking=True)
            mask   = batch['attention_mask'].to(device, non_blocking=True)

            optimizer.zero_grad(set_to_none=True)

            with autocast(enabled=cfg.use_amp):
                img_f, txt_f, scale = model(images, ids, mask)
                loss = loss_fn(img_f, txt_f, scale)

            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            nn.utils.clip_grad_norm_(model.parameters(), cfg.grad_clip)
            scaler.step(optimizer)
            scaler.update()
            scheduler.step()

            run_loss += loss.item()
            pbar.set_postfix({'loss': f'{run_loss/(pbar.n+1):.4f}',
                               'scale': f'{scale.item():.2f}'})

        avg_loss = run_loss / len(ssl_loader)
        is_best  = avg_loss < best_loss
        if is_best: best_loss = avg_loss

        # ── Save every epoch ──────────────────────────────────────
        ckpt_mgr.save(model, optimizer, epoch+1,
                       metrics={'ssl_loss': avg_loss},
                       scheduler=scheduler,
                       higher_is_better=False)

        print(f"  Epoch {epoch+1:02d}/{cfg.ssl_epochs}  loss={avg_loss:.4f}"
              f"  scale={scale.item():.2f}{'  ⭐ best' if is_best else ''}")

    print("\n✅ Stage 1 complete.")
    return model


