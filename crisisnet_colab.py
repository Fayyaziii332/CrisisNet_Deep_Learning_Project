"""## 🔍 Cell 14 — Cross-Attention Visualisation
Shows which image regions the model attends to for a given tweet.
"""

@torch.no_grad()
def visualise_attention(model, image_path: str, tweet_text: str,
                        save_path: str = None):
    """
    Overlays cross-attention weights (image→text) on the input image.
    Weights show which of the 7x7 spatial patches attend most to the tweet.
    """
    model.eval()

    # ── Load and transform image ──────────────────────────────────
    raw = Image.open(image_path).convert('RGB').resize((224, 224))
    img_tensor = EVAL_TF(raw).unsqueeze(0).to(device)

    enc  = tokenizer(tweet_text, max_length=cfg.max_len, padding='max_length',
                     truncation=True, return_tensors='pt')
    ids  = enc['input_ids'].to(device)
    mask = enc['attention_mask'].to(device)

    # ── Forward pass with attention ───────────────────────────────
    if isinstance(model, CrisisNetDANN):
        task_out, dom_out, attn_w = model(img_tensor, ids, mask, alpha=0.0, return_attn=True)
        logits = task_out
    else:
        logits, attn_w = model(img_tensor, ids, mask, return_attn=True)

    # attn_w: (1, 49, L) — average over text dim → (49,) → reshape (7, 7)
    attn = attn_w[0].mean(dim=-1).cpu().numpy().reshape(7, 7)
    attn = (attn - attn.min()) / (attn.max() - attn.min() + 1e-8)

    # ── Decode predictions ────────────────────────────────────────
    info_pred = logits['informative'].argmax(1).item()
    dmg_pred  = logits['damage'].argmax(1).item()
    hum_pred  = logits['humanitarian'].argmax(1).item()
    inv_info  = {v:k for k,v in INFORM_MAP.items()}
    inv_dmg   = {v:k for k,v in DAMAGE_MAP.items()}
    inv_hum   = {v:k for k,v in HUMAN_MAP.items()}

    # ── Plot ──────────────────────────────────────────────────────
    fig, axes = plt.subplots(1, 3, figsize=(14, 4))

    axes[0].imshow(raw)
    axes[0].set_title('Input image', fontsize=10)
    axes[0].axis('off')

    axes[1].imshow(raw)
    axes[1].imshow(np.kron(attn, np.ones((32, 32))),
                   alpha=0.55, cmap='hot', vmin=0, vmax=1)
    axes[1].set_title('Cross-attention (image→text)', fontsize=10)
    axes[1].axis('off')

    axes[2].axis('off')
    pred_text = (
        f"Tweet:\n{tweet_text[:120]}...\n\n"
        f"Informative : {inv_info.get(info_pred,'?')}\n"
        f"Damage      : {inv_dmg.get(dmg_pred,'?')}\n"
        f"Humanitarian: {inv_hum.get(hum_pred,'?')}"
    )
    axes[2].text(0.05, 0.5, pred_text, transform=axes[2].transAxes,
                 fontsize=8, va='center', wrap=True,
                 bbox=dict(boxstyle='round', facecolor='lightyellow', alpha=0.8))

    plt.suptitle('CrisisNet — Cross-Attention Visualisation', fontsize=12, fontweight='bold')
    plt.tight_layout()
    if save_path:
        plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.show()


