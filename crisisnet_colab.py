    scheduler    = get_linear_schedule_with_warmup(
        optimizer,
        num_warmup_steps  = max(1, int(cfg.ft_warmup * total_steps)),
        num_training_steps= total_steps,
    )
    scaler = GradScaler(enabled=cfg.use_amp)

    # ── Resume ────────────────────────────────────────────────────
    start_epoch = 0
    if resume:
        start_epoch, prev = ckpt_mgr.resume(model, optimizer, scheduler)

    for epoch in range(start_epoch, cfg.ft_epochs):
        model.train()
        run_loss = 0.0
