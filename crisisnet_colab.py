def load_best_model(stage='dann'):
    """Loads the best checkpoint for a given stage."""
    names = {'ssl':'clip', 'finetune':'crisisnet', 'dann':'crisisnet_dann'}
    ModelClass = {'ssl': CLIPModel, 'finetune': CrisisNet, 'dann': CrisisNetDANN}

    m = ModelClass[stage](cfg).to(device)
    ckpt_mgr = CheckpointManager(stage, names[stage])
    ckpt_mgr.resume(m, best=True)
    m.eval()
    return m


def predict(image_path_or_url: str, tweet_text: str,
            model=None, verbose=True):
    """
    Classify a crisis tweet-image pair.

    Returns dict with keys: informative, damage, humanitarian,
    plus confidence scores for each class.
    """
    if model is None:
        model = load_best_model('dann')

    # ── Load image ────────────────────────────────────────────────
    if image_path_or_url.startswith('http'):
        import urllib.request, io
        data = urllib.request.urlopen(image_path_or_url).read()
        raw  = Image.open(io.BytesIO(data)).convert('RGB')
    else:
        raw = Image.open(image_path_or_url).convert('RGB')

    img_t = EVAL_TF(raw).unsqueeze(0).to(device)

    # ── Tokenise ──────────────────────────────────────────────────
    enc = tokenizer(tweet_text, max_length=cfg.max_len, padding='max_length',
                    truncation=True, return_tensors='pt')
    ids  = enc['input_ids'].to(device)
    mask = enc['attention_mask'].to(device)

    # ── Inference ─────────────────────────────────────────────────
    with torch.no_grad():
        if isinstance(model, CrisisNetDANN):
            logits, _, _ = model(img_t, ids, mask, alpha=0.0, return_attn=True)
        else:
            logits, _ = model(img_t, ids, mask, return_attn=True)

    def decode(logit_vec, inv_map):
        probs = F.softmax(logit_vec, dim=-1).squeeze()
        pred  = probs.argmax().item()
        return inv_map.get(pred, 'unknown'), {inv_map.get(i,'?'): f'{p:.3f}'
                                               for i, p in enumerate(probs.tolist())}

    inv_info = {v:k for k,v in INFORM_MAP.items()}
    inv_dmg  = {v:k for k,v in DAMAGE_MAP.items()}
    inv_hum  = {v:k for k,v in HUMAN_MAP.items()}

    r_info, c_info = decode(logits['informative'], inv_info)
    r_dmg,  c_dmg  = decode(logits['damage'],      inv_dmg)
    r_hum,  c_hum  = decode(logits['humanitarian'], inv_hum)

    result = {
        'informative':    r_info,  'informative_conf':    c_info,
        'damage':         r_dmg,   'damage_conf':         c_dmg,
        'humanitarian':   r_hum,   'humanitarian_conf':   c_hum,
    }

    if verbose:
        print(f"\nTweet       : {tweet_text[:100]}")
        print(f"Informative : {r_info}  (conf: {c_info.get(r_info,'?')})")
        print(f"Damage      : {r_dmg}   (conf: {c_dmg.get(r_dmg,'?')})")
        print(f"Humanitarian: {r_hum}   (conf: {c_hum.get(r_hum,'?')})")

    return result


