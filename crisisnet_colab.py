def train_finetune(ssl_model_or_path=None, resume: bool = True):
    """Stage 2: supervised multi-task fine-tuning."""
    print("\n" + "="*60)
    print("STAGE 2 — Multi-task Fine-tuning")
    print("="*60)

    model    = CrisisNet(cfg).to(device)
    loss_fn  = MultiTaskLoss(cfg.lam_info, cfg.lam_dmg, cfg.lam_hum)
    ckpt_mgr = CheckpointManager('finetune', 'crisisnet')

    # ── Transfer SSL weights ────────────────────────────────────
    if ssl_model_or_path is not None:
        if isinstance(ssl_model_or_path, CLIPModel):
            transfer_ssl_weights(ssl_model_or_path, model)
