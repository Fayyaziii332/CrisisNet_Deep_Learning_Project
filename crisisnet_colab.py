def load_image(image_dir: str, image_id: str, tf) -> torch.Tensor:
    """Loads an image; returns a black tensor on failure."""
    # Try direct path, then event subdirectory
    candidates = [
        os.path.join(image_dir, f'{image_id}.jpg'),
        os.path.join(image_dir, f'{image_id}.png'),
    ]
    # Also search event sub-folders
    for ev in EVENTS:
        candidates.append(os.path.join(image_dir, ev, f'{image_id}.jpg'))

    for p in candidates:
        if os.path.exists(p):
            try:
                return tf(Image.open(p).convert('RGB'))
            except Exception:
                pass
    return torch.zeros(3, cfg.img_size, cfg.img_size)


